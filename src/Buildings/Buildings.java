package Buildings;

import townwars.Town;

public class Buildings {
	
	Town stadtname;
	String buildingsName;
	int buildinghp;
	
	
	
	public Town getStadtname() {
		return stadtname;
	}
	public void setStadtname(Town stadtname) {
		this.stadtname = stadtname;
	}
	public String getBuildingsName() {
		return buildingsName;
	}
	public void setBuildingsName(String buildingsName) {
		this.buildingsName = buildingsName;
	}
	public int getBuildinghp() {
		return buildinghp;
	}
	public void setBuildinghp(int buildinghp) {
		this.buildinghp = buildinghp;
	}
	

	
	
	
	
}
